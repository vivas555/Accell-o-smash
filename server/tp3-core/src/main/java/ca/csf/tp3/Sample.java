package ca.csf.tp3;

public class Sample {

    private int id;
    private String info;

    public Sample() {
        id = 0;
        info = null;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getInfo() {
        return info;
    }

    public void setInfo(String info) {
        this.info = info;
    }

}
